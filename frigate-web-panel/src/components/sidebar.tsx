export function Sidebar() {
  const pathname = usePathname();
  const { user, logout } = useAuth();
  const [mobileOpen, setMobileOpen] = useState(false);

  const visibleItems = NAV_ITEMS.filter(
    (item) => !item.adminOnly || user?.role === "admin"
  );

  const sidebarContent = (
    <>
      <div className="px-5 py-6">
        <h1 className="text-lg font-bold text-cyan-400">Frigate Intelligence</h1>
        <p className="text-xs text-gray-500 mt-1">پنل مدیریت</p>
      </div>

      <nav className="flex-1 px-3 space-y-1">
        {visibleItems.map((item) => {
          const active = pathname === item.href || pathname.startsWith(item.href + "/");
          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={() => setMobileOpen(false)}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-2xl text-sm transition-colors ${
                active
                  ? "bg-cyan-600 text-white"
                  : "text-gray-400 hover:text-gray-200 hover:bg-gray-800"
              }`}
            >
              <span className={active ? "text-white" : "text-cyan-400"}>{item.icon}</span>
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="px-5 py-4 border-t border-gray-800 space-y-3">
        <HealthBadge />
        <div className="flex items-center justify-between text-xs text-gray-500">
          <span>{user?.username} ({user?.role})</span>
          <button
            onClick={logout}
            className="text-gray-400 hover:text-red-400 transition-colors"
          >
            خروج
          </button>
        </div>
      </div>
    </>
  );

  return (
    <>
      {/* Mobile hamburger */}
      <button
        onClick={() => setMobileOpen(true)}
        className="md:hidden fixed top-4 left-4 z-50 p-2 bg-gray-800 rounded-xl text-gray-300"
        aria-label="Open menu"
      >
        <svg className="w-6 h-6" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
          <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
        </svg>
      </button>

      {/* Mobile overlay */}
      {mobileOpen && (
        <div
          className="md:hidden fixed inset-0 bg-black/50 z-40"
          onClick={() => setMobileOpen(false)}
        />
      )}

      {/* Mobile sidebar */}
      <aside
        className={`md:hidden fixed top-0 right-0 h-full w-64 bg-gray-900 z-50 flex flex-col transform transition-transform ${
          mobileOpen ? "translate-x-0" : "translate-x-full"
        }`}
      >
        {sidebarContent}
      </aside>

      {/* Desktop sidebar (RTL: on the right) */}
      <aside className="hidden md:flex w-64 bg-gray-900 flex-col border-l border-gray-800 shrink-0">
        {sidebarContent}
      </aside>
    </>
  );
}
